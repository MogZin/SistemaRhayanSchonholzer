    private void loginSucesso() {
        JOptionPane.showMessageDialog(this, "Login realizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        JFrmPrincipal jFrmPrincipal = new JFrmPrincipal();
        jFrmPrincipal.setVisible(true);
        this.dispose();
    }

    private void loginFalha() {
        tentativas++;
        if (tentativas >= MAX_TENTATIVAS) {
            JOptionPane.showMessageDialog(this,
                    "Número máximo de tentativas excedido!\nSistema será bloqueado.",
                    "Acesso Bloqueado",
                    JOptionPane.ERROR_MESSAGE);
            habilitar(false);
            System.exit(0);
        } else {
            JOptionPane.showMessageDialog(this,
                    "Usuário ou senha inválidos!\nTentativa " + tentativas + " de " + MAX_TENTATIVAS,
                    "Falha no Login",
                    JOptionPane.WARNING_MESSAGE);
            limpar();
            jTxtUsuario.requestFocus();
        }
    }


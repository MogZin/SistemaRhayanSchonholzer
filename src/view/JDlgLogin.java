    private void jBtnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEntrarActionPerformed

        String apelido = jTxtUsuario.getText().trim();
        String senha = new String(jPwfSenha.getPassword()).trim();

        // Validação básica dos campos
        if (apelido.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            // Verifica o login usando Hibernate
            RpsUsuarios usuario = usuariosDAO.verificarLogin(apelido, senha);

            if (usuario != null && "S".equals(usuario.getRpsAtivo())) {
                loginSucesso();
            } else {
                loginFalha();
            }
        } catch (HibernateException e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao conectar ao banco de dados: " + e.getMessage(),
                    "Erro de Conexão", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jBtnEntrarActionPerformed

